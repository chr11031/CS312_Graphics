/*************************************************************
 * CROSS_PRODUCT
 * Does the determinant function AD-BC. Essential 
 * building block for most of drawing.
 ************************************************************/
float crossProduct(Vertex v1, Vertex v2)
{
    float ad = v1.x * v2.y;
    float bc = v1.y * v2.x;
    float determinant = ad - bc;

    return determinant;
}

